public class Arithmetic {
   public static void main(String[] args) {
      int a = 35;
      int b = 4;
      int sum = a + b;
      int dif = a- b;
      int prod = a * b;
      int quot = a / b;
      int rem = a % b;
      System.out.println(a + " + " + b + " = " + sum);
      System.out.println(a + " + " + b + " = " + dif);
      System.out.println(a + " * " + b + " = " + prod);
      System.out.println(a + " / " + b + " = " + quot);
      System.out.println(a + " % " + b + " = " + rem); 
   }
}